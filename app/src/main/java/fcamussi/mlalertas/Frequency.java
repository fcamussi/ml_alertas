package fcamussi.mlalertas;

/**
 * Clase Frequency
 *
 * @author Fernando Camussi
 */
public class Frequency {

    private String id;
    private int minutes;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

}
